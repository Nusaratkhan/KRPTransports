const langLabels = { en: "EN", ta: "தமிழ்", hi: "हिंदी" };
let currentLang = "en";

function applyLang(lang) {
  currentLang = lang;
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const k = el.getAttribute("data-i18n");
    if (translations[lang][k]) el.textContent = translations[lang][k];
  });
  document.getElementById("langLabel").textContent = langLabels[lang];
  document.querySelectorAll("#langMenu button").forEach(b => {
    b.classList.toggle("active-lang", b.dataset.lang === lang);
  });
}

const langBtn = document.getElementById("langBtn");
const langMenu = document.getElementById("langMenu");
langBtn.addEventListener("click", e => { e.stopPropagation(); langMenu.classList.toggle("open"); });
document.addEventListener("click", () => langMenu.classList.remove("open"));
langMenu.querySelectorAll("button").forEach(b => {
  b.addEventListener("click", () => { applyLang(b.dataset.lang); langMenu.classList.remove("open"); });
});

const darkToggle = document.getElementById("darkToggle");
const moonIcon = document.getElementById("moonIcon");
const sunIcon = document.getElementById("sunIcon");
darkToggle.addEventListener("click", () => {
  const dark = document.documentElement.classList.toggle("dark");
  moonIcon.style.display = dark ? "none" : "";
  sunIcon.style.display = dark ? "" : "none";
});

const hamburger = document.getElementById("hamburger");
const mobileNav = document.getElementById("mobileNav");

if (hamburger && mobileNav) {
  hamburger.addEventListener("click", () => {
    mobileNav.classList.toggle("open");
  });

  mobileNav.querySelectorAll("a").forEach(a => {
    a.addEventListener("click", () => {
      mobileNav.classList.remove("open");
    });
  });
}

const scrollBtn = document.getElementById("scrollTop");
window.addEventListener("scroll", () => {
  scrollBtn.classList.toggle("visible", window.scrollY > 300);
});
scrollBtn.addEventListener("click", () => window.scrollTo({ top: 0, behavior: "smooth" }));

